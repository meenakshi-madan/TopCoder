  public int getPersistence(int n)
	{
		ArrayList<Integer> seq = new ArrayList<Integer>();
		int prev = n;
		seq.add(prev);
		while(prev >= 10)
		{
			prev = getProduct(prev);
			seq.add(prev);
		}
		
		return (seq.size() - 1);
	}
	
	private int getProduct(int x)
	{
		int product = 1, divisor = 10, rem=x;
		while(rem > 0)
		{
			product *= rem%divisor;
			rem /= 10;
		}
		
		return product;
	}
	
